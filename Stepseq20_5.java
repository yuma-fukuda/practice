
public class Stepseq20_5 {
	public static void main(String[] args) {
	//変数の宣言
		boolean a = false;
		char b = 'A';
		String c = "かきくけこ";
		byte d = 58;
		short e =8252 ;
		int f = 285995;
		long g = 629940959;
		double h = 6.637;
		float i = 0.21F;
	//値の出力
		System.out.println("BOOLの変数  ：" + a);
		System.out.println("CHARの変数  ：" + b);
		System.out.println("STRの変数   ：" + c);
		System.out.println("BYTEの変数  ：" + d);
		System.out.println("SHORTの変数 ：" + e);
		System.out.println("INTの変数   ：" + f);
		System.out.println("LONGの変数  ：" + g);
		System.out.println("DOUBLEの変数：" + h);
		System.out.println("FLOATの変数 ：" + i);
	}

}
