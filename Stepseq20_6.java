
public class Stepseq20_6 {
	public static void main(String[] args) {
		int num1 = 5 ;        //1つ目の変数num1を定義
		int num2 = -4 ;       //2つ目の変数num2を定義

		System.out.println(-num1);   //単項演算子を使用し正負を反転
		System.out.println(-num2);   //単項演算子を使用し正負を反転
	}

}

